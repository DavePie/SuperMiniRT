int	ft_atoi(const char *str)
{
	int	ans;
	int	sign;

	ans = 0;
	sign = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '+' || *str == '-')
		if (*(str++) == '-')
			sign *= -1;
	while (*str >= '0' && *str <= '9')
		ans = ans * 10 + sign * (*(str++) - '0');
	return (ans);
}
